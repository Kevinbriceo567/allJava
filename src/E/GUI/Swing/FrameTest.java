    private void btnSumarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSumarMouseClicked
        
        try
        {
            if (txfS1.getText()!="" && txfS2.getText()!=""){
                int field1 = Integer.parseInt(txfS1.getText());
                int field2 = Integer.parseInt(txfS2.getText());
                int result = field1 + field2;

                lbResult.setText(Integer.toString(result));
            }
        }
        catch(Exception e){
                JOptionPane.showMessageDialog(null,
                "Two numbers are required",
                "Warning",
                JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_btnSumarMouseClicked

